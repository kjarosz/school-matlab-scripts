function setFigureProperties(fig)
    set(fig, 'defaultLineLineWidth', 1.5);
    set(fig, 'DefaultTextFontSize', 16);
    set(fig, 'DefaultAxesXColor', [0, 0, 0]);
    set(fig, 'DefaultAxesYColor', [0, 0, 0]);
    set(fig, 'DefaultAxesFontSize', 16);
    set(fig, 'DefaultAxesXGrid', 'on');
    set(fig, 'DefaultAxesYGrid', 'on');
end